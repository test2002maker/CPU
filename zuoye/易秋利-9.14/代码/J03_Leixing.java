package test1;

public class J03_Leixing {
	public static void main(String[] args) {
		//整型 byte(-128-127)
		byte a=100;
		
		//整型 short
		short b=20;
		
		//int
		int c=2000;
		
		//long
		long l=342125432;
		
		//float //单精度
		float f=2.903f;
		
		//double //双精度
		double d=29.37;
		
		//字符型
		char c1='a';
		
		//boolean
		//不能赋数字型的变量
		boolean bo=true;
		
		//字符串--类
		String name="testing";
		System.out.println(name);
	}

}
